public class ArithmetischeOperationen {

    public static void main(String[] args) {
        int a = 4;
        int b = 3;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        //falsch, nur als Beispiel
        int x = 2_0000_0000;
        int y = 2_0000_0000;

        System.out.println(x + y);
    }
}
